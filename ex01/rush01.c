void	ft_putchar(char c);

void	rush(int x, int y)
{
	int		a;
	int		b;
	char	c;

	a = x;
	b = y;
	while (y > 0)
	{
		while (x > 0)
		{
			c = ' ';
			if ((x == 1 && y==1 && b > 1 && a > 1) || (b == y && x == a))
				c = '/';
			else if ((a == x && y == 1) || (x == 1 && y == b))
				c = '\\';
			else if (((y ==  b  || y ==  1)  &&  (x >1 || x < a)) || (x == a || x == 1))
				c = '*';
			ft_putchar(c);
			--x;
		}
		x = a;
		--y;
		ft_putchar('\n');
	}
}
