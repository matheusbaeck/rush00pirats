void	ft_putchar(char c);

void	rush(int x, int y)
{
	int		a;
	int		b;
	char	c;

	a = x;
	b = y;
	while (y > 0)
	{
		while (x > 0)
		{
			c = ' ';
			if ((a == x || x == 1) && (b == y || y == 1))
				c = 'o';
			else if ((a == x || x == 1) && (b != y && y != 1))
				c = '|';
			else if (b == y || y == 1)
				c = '-';
			ft_putchar(c);
			--x;
		}
		x = a;
		--y;
		ft_putchar('\n');
	}
}
